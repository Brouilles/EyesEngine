package gameplay;

public class EntityGun extends Entity{
	public EntityBall m_ball = new EntityBall(10);
	
	public EntityGun()
	{	}
	
	public void shoot()
	{
		m_ball.ballShoot(m_x, m_y);
		
		if(m_look == 0)
		{
			m_ball.draw(0);
		}
	}
	
	public void addBallTexture(String pos)
	{
		m_ball.addBallTexture(pos);
	}
}

